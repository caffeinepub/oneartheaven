export const getAllNotifications = (): Notification[] => [
  ...SEED_NOTIFICATIONS,
];
export const getNotificationsByOrg = (orgId: string): Notification[] =>
  SEED_NOTIFICATIONS.filter((n) => n.orgId === orgId);
export const getUnreadNotifications = (orgId?: string): Notification[] =>
  (orgId ? getNotificationsByOrg(orgId) : SEED_NOTIFICATIONS).filter(
    (n) => n.status === "unread",
  );
export const getNotificationsByType = (type: string): Notification[] =>
  SEED_NOTIFICATIONS.filter((n) => n.type === type);
export const getAllThreads = (): MessageThread[] => [...SEED_THREADS];
export const getThread = (id: string): MessageThread | undefined =>
  SEED_THREADS.find((t) => t.id === id);
export const getMessagesByThread = (threadId: string): DirectMessage[] =>
  SEED_MESSAGES.filter((m) => m.threadId === threadId);
export const getActiveThreads = (): MessageThread[] =>
  SEED_THREADS.filter((t) => !t.isArchived);
export const getPinnedThreads = (): MessageThread[] =>
  SEED_THREADS.filter((t) => t.isPinned && !t.isArchived);
