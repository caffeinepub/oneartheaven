const TIER_ORDER = ["Seed", "Growth", "Scale", "Global"];
const TIER_MILESTONES: Record<string, number> = {
  Seed: 0,
  Growth: 25000,
  Scale: 100000,
  Global: 500000,
};

function buildStats(vendorId: string): VendorStats {
  const listings = SEED_LISTINGS.filter((l) => l.vendorId === vendorId);
  const revenue = SEED_REVENUE.filter((r) => r.vendorId === vendorId);
  const vendor = SEED_VENDORS.find((v) => v.id === vendorId)!;
  const totalGross = revenue.reduce((s, r) => s + r.grossRevenue, 0);
  const totalFF = revenue.reduce((s, r) => s + r.finfracfranShare, 0);
  const totalNet = revenue.reduce((s, r) => s + r.netRevenue, 0);
  const pending = revenue
    .filter((r) => r.status === "pending")
    .reduce((s, r) => s + r.netRevenue, 0);
  const tierIdx = TIER_ORDER.indexOf(vendor.tier);
  const nextTier = TIER_ORDER[tierIdx + 1];
  const nextMilestone = nextTier
    ? TIER_MILESTONES[nextTier]
    : Number.POSITIVE_INFINITY;
  return {
    vendorId,
    activeListings: listings.filter((l) => l.status === "active").length,
    totalListings: listings.length,
    totalGrossRevenue: totalGross,
    totalFinfracfranEarnings: totalFF,
    totalNetRevenue: totalNet,
    nationsReached: Math.max(...listings.map((l) => l.nationsCount)),
    pendingPayouts: pending,
    currency: "USD",
    currentTier: vendor.tier,
    nextTierTarget: nextMilestone,
  };
}

export const VENDOR_STATS: Record<string, VendorStats> = Object.fromEntries(
  SEED_VENDORS.map((v) => [v.id, buildStats(v.id)]),
);

export function getAllVendors(): VendorProfile[] {
  return SEED_VENDORS;
}
export function getVendor(vendorId: string): VendorProfile | undefined {
  return SEED_VENDORS.find((v) => v.id === vendorId);
}
export function getVendorsByTier(tier: VendorProfile["tier"]): VendorProfile[] {
  return SEED_VENDORS.filter((v) => v.tier === tier);
}
export function getActiveVendors(): VendorProfile[] {
  return SEED_VENDORS.filter((v) => v.status === "active");
}
export function getVendorListings(
  vendorId: string,
  type?: VendorListing["type"],
  status?: VendorListing["status"],
): VendorListing[] {
  return SEED_LISTINGS.filter(
    (l) =>
      l.vendorId === vendorId &&
      (type === undefined || l.type === type) &&
      (status === undefined || l.status === status),
  );
}
export function getAllListings(): VendorListing[] {
  return SEED_LISTINGS;
}
export function getVendorRevenue(
  vendorId: string,
  period?: VendorRevenueRecord["period"],
): VendorRevenueRecord[] {
  return SEED_REVENUE.filter(
    (r) =>
      r.vendorId === vendorId && (period === undefined || r.period === period),
  );
}
export function getVendorStats(vendorId: string): VendorStats | undefined {
  return VENDOR_STATS[vendorId];
}
