// ---------------------------------------------------------------------------
// Helper Functions
// ---------------------------------------------------------------------------

export function getAllSDGs(): SDGoal[] {
  return SDG_GOALS;
}

export function getSDG(number: SDGoalNumber): SDGoal | undefined {
  return SDG_GOALS.find((g) => g.number === number);
}

export function getMetricsByOrg(orgId: string): ImpactMetric[] {
  return IMPACT_METRICS.filter((m) => m.orgId === orgId);
}

export function getMetricsBySDG(sdgNumber: SDGoalNumber): ImpactMetric[] {
  return IMPACT_METRICS.filter((m) => m.linkedSDGs.includes(sdgNumber));
}

export function getReportsByOrg(orgId: string): ImpactReport[] {
  return IMPACT_REPORTS.filter((r) => r.orgId === orgId);
}

export function getPublishedReports(): ImpactReport[] {
  return IMPACT_REPORTS.filter((r) => r.status === "published").sort(
    (a, b) =>
      new Date(b.publishedAt ?? 0).getTime() -
      new Date(a.publishedAt ?? 0).getTime(),
  );
}

export function getOrgImpactProfile(
  orgId: string,
): OrgImpactProfile | undefined {
  return ORG_IMPACT_PROFILES.find((p) => p.orgId === orgId);
}

export function getPlatformAggregate(): PlatformImpactAggregate {
  return PLATFORM_IMPACT_AGGREGATE;
}
